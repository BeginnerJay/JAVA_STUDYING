package chapter4;

public class IfElseIf {
	public static void main(String[] args) {

        int age = 16;

        if (age >= 19) {

            System.out.println("성인입니다.");

        } else if (age > 12) {

            System.out.println("청소년입니다.");

        } else {

            System.out.println("어린애입니다.");

        }
   }
}
